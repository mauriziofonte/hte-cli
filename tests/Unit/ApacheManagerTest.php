    /**
     * @test
     */
    public function testNormalizeIndexes()
    {
        // Gaps: 001, 003, 005
        $this->fs->putContents(
            '/test/sites-available/001-first.test.conf',
            $this->buildHteConf('first.test', '/var/www/first', '8.0', false, false)
        );
        $this->fs->putContents(
            '/test/sites-available/003-third.test.conf',
            $this->buildHteConf('third.test', '/var/www/third', '8.1', false, false)
        );
        $this->fs->putContents(
            '/test/sites-available/005-fifth.test.conf',
            $this->buildHteConf('fifth.test', '/var/www/fifth', '8.2', false, false)
        );

        $result = $this->apache->normalizeIndexes();

        $this->assertSame(2, $result['renamed']);
        $this->assertEmpty($result['errors']);

        // Renamed files must exist; original gap filenames must be gone
        $this->assertTrue($this->fs->fileExists('/test/sites-available/002-third.test.conf'));
        $this->assertTrue($this->fs->fileExists('/test/sites-available/003-fifth.test.conf'));
        $this->assertFalse($this->fs->fileExists('/test/sites-available/003-third.test.conf'));
        $this->assertFalse($this->fs->fileExists('/test/sites-available/005-fifth.test.conf'));
    }

