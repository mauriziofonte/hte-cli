    /**
     * Build a minimal but realistic HTE-CLI Apache config string.
     *
     * @param string $domain
     * @param string $docroot
     * @param string $phpVersion
     * @param bool   $ssl
     * @param bool   $forceHttps
     * @return string
     */
    private function buildHteConf($domain, $docroot, $phpVersion, $ssl = false, $forceHttps = false)
    {
        $marker = '# Auto-generated by HTE-Cli on 2025-01-01 00:00:00';
        $handler = 'SetHandler "proxy:unix:/var/run/php/php' . $phpVersion . '-fpm-' . $domain . '.sock|fcgi://localhost/"';
        $forceBlock = $forceHttps
            ? "    # Auto-generated Force HTTPS\n    RewriteEngine On\n    RewriteCond %{HTTPS} off\n    RewriteRule ^/(.*) https://%{HTTP_HOST}%{REQUEST_URI} [R=301,L]"
            : '';

        $conf = <<<CONF
<VirtualHost 127.0.0.1:80>
    {$marker}
    ServerName {$domain}
    ServerAlias www.{$domain}
    DocumentRoot {$docroot}
    ServerAdmin admin@localhost.net

    <FilesMatch \.php$>
        {$handler}
    </FilesMatch>

{$forceBlock}
</VirtualHost>

CONF;

        if ($ssl) {
            $conf .= <<<CONF
<IfModule mod_ssl.c>
    <VirtualHost 127.0.0.1:443>
        {$marker}
        ServerName {$domain}
        DocumentRoot {$docroot}

        <FilesMatch \.php$>
            {$handler}
        </FilesMatch>

        SSLEngine on
        SSLCertificateFile "/test/certs/{$domain}.crt"
        SSLCertificateKeyFile "/test/certs/{$domain}.key"
    </VirtualHost>
</IfModule>

CONF;
        }

        return $conf;
    }

