    /**
     * @test
     */
    public function testNormalizeIndexesUpdatesSymlinks()
    {
        // 001 is enabled (symlink in sites-enabled), 003 has a gap and is also enabled
        $this->fs->putContents(
            '/test/sites-available/001-a.test.conf',
            $this->buildHteConf('a.test', '/var/www/a', '8.0', false, false)
        );
        $this->fs->putContents(
            '/test/sites-available/003-b.test.conf',
            $this->buildHteConf('b.test', '/var/www/b', '8.1', false, false)
        );

        // Simulate enabled site via symlink
        $this->fs->symlink(
            '/test/sites-available/003-b.test.conf',
            '/test/sites-enabled/003-b.test.conf'
        );

        $result = $this->apache->normalizeIndexes();

        $this->assertSame(1, $result['renamed']);

        // Old symlink must be gone; new symlink must point at the renamed file
        $this->assertFalse($this->fs->isLink('/test/sites-enabled/003-b.test.conf'));
        $this->assertTrue($this->fs->isLink('/test/sites-enabled/002-b.test.conf'));

        $links = $this->fs->getLinks();
        $this->assertSame('/test/sites-available/002-b.test.conf', $links['/test/sites-enabled/002-b.test.conf']);
    }

