        $htaccess = <<<HTACCESS
RewriteEngine On

# Alternative HTTPS redirect pattern
RewriteCond %{HTTPS} !=on
RewriteRule ^(.*)$ https://%{SERVER_NAME}%{REQUEST_URI} [R=301,L]
HTACCESS;

        $docroot = '/var/www/altapp';
        $this->fs->putContents($docroot . '/.htaccess', $htaccess);

        $result = $this->apache->hasHtaccessHttpsRedirect($docroot);

        $this->assertTrue($result['has_redirect']);
        $this->assertSame($docroot . '/.htaccess', $result['file']);
    }
}
